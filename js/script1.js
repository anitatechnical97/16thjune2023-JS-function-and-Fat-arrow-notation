// There are two ways you can define the function.

/*
1. Classical way
  using function keyword

2. New Way 2015 ES6 (Fat Arrow => notation)
*/


// 1. Old Way 1995-2023

// 1. Function definition

    function aFunction() {
        console.log("Hello from aFunction");

        //  Every function return something.
    }


// 2. Calling function
    aFunction();


// 2. New Way 2015 ES6 (Fat Arrow => notation)
// 1. Function definition 

    var bFunction=()=> {        //  New style 2015 ES6 (Fat Arrow => notation)
      console.log('Hello from bFunction');

      //  Every function return something.

    }

// 2. Calling function

     bFunction();